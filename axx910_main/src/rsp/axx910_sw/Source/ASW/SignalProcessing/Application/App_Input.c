void App_vInputProcessing(void)
{
	App_vTrkInput(Sp_TrackOut);

}


/**************************************************************************************
* ------------------------------   static functions   ------------------------------- *
**************************************************************************************/
static void App_vTrkInput(SpTrackOut_t Trk[SP_NUM_TRACK_MAX])
{
	int16_t tr;


	for (tr = 0; tr < SP_NUM_TRACK_MAX; tr++)
	{
		gAppTrack[tr].Status = Trk[tr].u8Status;
		if (gAppTrack[tr].Status == VERIFIED_TARGET)
		{
			gAppTrack[tr].MovState = Trk[tr].u8MovState;
			gAppTrack[tr].f32PosX = Trk[tr].f32PosX_m;
			gAppTrack[tr].f32PosY = Trk[tr].f32PosY_m;
			gAppTrack[tr].f32VelX = Trk[tr].f32VelX_mps;
			gAppTrack[tr].f32VelY = Trk[tr].f32VelY_mps;
			gAppTrack[tr].f32Pow_dB = Trk[tr].f32Power_dB;
		}
		else
		{
			gAppTrack[tr].MovState = STATIONARY;
			gAppTrack[tr].f32PosX = 0.0f;
			gAppTrack[tr].f32PosY = 0.0f;
			gAppTrack[tr].f32VelX = 0.0f;
			gAppTrack[tr].f32VelY = 0.0f;
			gAppTrack[tr].f32Pow_dB = 0.0f;
		}
	}

}


